package clicker.v4.wrappers;

import java.util.ArrayList;

public class QuizResponse {
	private int centerid, quizid;
	private String workshopid;
	private ArrayList<Participant> participant;
	public int getCenterid() {
		return centerid;
	}
	public void setCenterid(int centerid) {
		this.centerid = centerid;
	}
	public int getQuizrecordid() {
		return quizid;
	}
	public void setQuizid(int quizid) {
		this.quizid = quizid;
	}
	public String getWorkshopid() {
		return workshopid;
	}
	public void setWorkshopid(String workshopid) {
		this.workshopid = workshopid;
	}
	public ArrayList<Participant> getParticipant() {
		return participant;
	}
	public void setParticipant(ArrayList<Participant> participant) {
		this.participant = participant;
	}
	

}

